				bahan yang menyusun dinding sel tumbuhan, tetapi tersusun dari 
				<a href="https://id.wikipedia.org/wiki/Kitin" target="_blank" title="Kitin adalah polisakarida struktural yang digunakan untuk menyusun eksoskleton dari artropoda (serangga, laba-laba, krustase, dan hewan-hewan lain sejenis).">kitin</a>, 
				yang malah kebanyakan dihasilkan 
				<a href="https://id.wikipedia.org/wiki/Hewan" target="_blank" title="Hewan, binatang, fauna, margasatwa, atau satwa adalah organisme eukariotik multiseluler yang membentuk kerajaan biologi Animalia.">hewan</a>.
