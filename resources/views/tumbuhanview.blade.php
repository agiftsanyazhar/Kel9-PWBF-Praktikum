			<p class="paragraf">
				Dalam 
				<a href="https://id.wikipedia.org/wiki/Biologi" target="_blank" title="Biologi adalah kajian tentang kehidupan, dan organisme hidup, termasuk struktur, fungsi, pertumbuhan, evolusi, persebaran, dan taksonominya.">biologi</a>, 
				<b>tumbuhan</b> 
				adalah 
				<a href="https://id.wikipedia.org/wiki/Makhluk_hidup" target="_blank" title="Dalam setiap pernyataan suatu kejadian menurut sistem biologi dan ekologi, organisme (bahasa Yunani:organon yang berarti alat) adalah kumpulan molekul-molekul yang saling memengaruhi sedemikian sehingga berfungsi secara stabil dan memiliki sifat hidup.">organisme</a>
				<a href="https://id.wikipedia.org/wiki/Eukariota" target="_blank" title="Eukariota adalah organisme dengan sel yang memiliki nukleus dan organel bermembran lainnya.">eukariota</a> 
				<a href="https://id.wikipedia.org/wiki/Organisme_multiseluler" target="_blank" title="Multiselular adalah suatu istilah biologi untuk organisme yang mempunyai banyak sel, kontras dengan organisme uniselular yang hanya mempunyai satu sel.">multiseluler</a> 
				yang tergolong ke dalam 
				<a href="https://id.wikipedia.org/wiki/Kerajaan_(biologi)" target="_blank" title="Dalam biologi, kerajaan (bahasa Inggris:Kingdom; Latin:regnum, pl. regna) adalah tingkatan paling atas dari tingkatan klasifikasi makhluk hidup.">kerajaan</a> 
				<a href="#top" title="Dalam biologi, tumbuhan adalah organisme eukariota multiseluler yang tergolong ke dalam kerajaan Plantae.">Plantae</a>. 
				Di dalamnya terdiri atas beberapa 
				<a href="https://id.wikipedia.org/wiki/Klad" target="_blank" title="Klad atau klade adalah suatu kelompok taksonomi yang memiliki satu leluhur bersama dan semua keturunannya juga berasal dari moyang tersebut.">klad</a> 
				yakni, 
				<a href="https://id.wikipedia.org/wiki/Tumbuhan_berbunga" target="_blank" title="Tumbuhan berbunga atau Anthophyta (&quot;tumbuhan bunga&quot;) atau Magnoliophyta (&quot;tumbuhan sekerabat dengan magnolia&quot;) adalah kelompok terbesar tumbuhan yang hidup di daratan.">tanaman berbunga</a>, 
				<a href="https://id.wikipedia.org/wiki/Tumbuhan_berbiji_terbuka" target="_blank" title="Gymnospermae (dari bahasa Yunani:gymnos (telanjang) dan sperma (biji) atau tumbuhan berbiji terbuka merupakan kelompok tumbuhan berbiji yang bijinya tidak terlindung dalam bakal buah (ovarium).">Gymnospermae</a> 
				atau 
				<a href="https://id.wikipedia.org/wiki/Tumbuhan_berbiji_terbuka" target="_blank" title="Gymnospermae (dari bahasa Yunani:gymnos (telanjang) dan sperma (biji) atau tumbuhan berbiji terbuka merupakan kelompok tumbuhan berbiji yang bijinya tidak terlindung dalam bakal buah (ovarium).">Tumbuhan berbiji terbuka</a>, 
				<a href="https://id.wikipedia.org/wiki/Lycopodiopsida" target="_blank" title="Lycopodiopsida mencakup berbagai jenis tumbuhan berpembuluh berspora dengan struktur pembuluh yang primitif dengan daun yang sempit dan cenderung duduk.">Lycopodiopsida</a>, 
				<a href="https://id.wikipedia.org/wiki/Tumbuhan_paku" target="_blank" title="Tumbuhan paku, paku-pakuan, atau pakis-pakisan adalah sekelompok tumbuhan dengan sistem pembuluh sejati (Tracheophyta) tetapi tidak pernah menghasilkan biji untuk reproduksi seksualnya.">paku-pakuan</a>, 
				<a href="https://id.wikipedia.org/wiki/Lumut_(disambiguasi)" target="_blank" title="Lumut dalam bahasa sehari-hari (kolokial) merujuk pada sekelompok vegetasi kecil yang tumbuh pada tempat lembap atau perairan dan biasanya tumbuh meluas menutupi permukaan.">lumut</a>, 
				serta sejumlah 
				<a href="https://id.wikipedia.org/wiki/Alga_hijau" target="_blank" title="Alga hijau adalah kelompok alga berdasarkan zat warna atau pigmentasinya.">alga hijau</a>.
			</p>
			<p class="paragraf">
				Tanaman hijau memiliki dinding sel yang kokoh mengandung 
				<a href="https://id.wikipedia.org/wiki/Selulosa" target="_blank" title="Selulosa merupakan senyawa organik dengan rumus (C6H10O5)n, sebuah polisakarida yang terdiri dari rantai linier dari beberapa ratus hingga lebih dari sepuluh ribu ikatan &beta;(1&rarr;4) unit D-glukosa.">selulosa</a>. 
				Hampir semua anggota tumbuhan bersifat 
				<i><a href="https://id.wikipedia.org/wiki/Autotrof" target="_blank" title="Organisme autotrof merupakan organisme yang dapat mengubah bahan anorganik menjadi organik (dapat membuat makanan sendiri) dengan bantuan energi seperti energi cahaya matahari dan kimia.">autotrof</a></i>, 
				yakni memproduksi energi sendiri dengan mengubah energi 
				<a href="https://id.wikipedia.org/wiki/Cahaya" target="_blank" title="Cahaya adalah energi berbentuk gelombang elektromagnetik yang kasat mata dengan panjang gelombang sekitar 380-750 nm.">cahaya</a> 
				<a href="https://id.wikipedia.org/wiki/Matahari" target="_blank" title="Matahari atau Surya adalah bintang di pusat Tata Surya.">matahari</a> 
				melalui proses yang disebut 
				<a href="https://id.wikipedia.org/wiki/Fotosintesis" target="_blank" title="Fotosintesis adalah suatu proses biokimia pembentukan zat makanan seperti karbohidrat yang dilakukan oleh tumbuhan, terutama tumbuhan yang mengandung zat hijau daun atau klorofil.">fotosintesis</a> 
				dalam organel sel bernama 
				<a href="https://id.wikipedia.org/wiki/Kloroplas" target="_blank" title="Kloroplas (bahasa Inggris:Chloroplast) adalah bagian dari plastid yang mengandung klorofil.">kloroplas</a>. 
				Karena warna hijau yang dominan pada anggota kerajaan ini, nama lain yang dipakai adalah 
				<b>Viridiplantae</b>
				(&quot;tetumbuhan hijau&quot;). Nama lainnya adalah 
				<b>Metaphyta</b>.
				Namun ada juga tumbuhan yang bersifat parasit dan beberapa sudah tidak memiliki kemampuan fotosintesis dengan sedikit atau bahkan tanpa 
				<a href="https://id.wikipedia.org/wiki/Klorofil" target="_blank" title="Klorofil (dari bahasa Inggris, chlorophyll) atau zat hijau daun (terjemah langsung dari bahasa Belanda, bladgroen) adalah pigmen yang dimiliki oleh berbagai organisme dan menjadi salah satu molekul berperan utama dalam fotosintesis.">klorofil</a>. 
				Tanaman juga bisa dikarakterisasi dari cara mereka berkembang biak, kemampuan pertumbuhan, dan 
				<a href="https://id.wikipedia.org/wiki/Pergiliran_keturunan" target="_blank" title="Pergiliran keturunan atau metagenesis adalah daur hidup yang dialami oleh organisme, yang untuk setiap fase/tahapnya melibatkan individu dengan kandungan genetik berbeda:biasanya tahap haploid (n) dan tahap diploid (2n).">pergiliran keturunan</a>.
			</p>
			<p class="paragraf">
				Tercatat sekitar 350.000 
				<a href="https://id.wikipedia.org/wiki/Spesies" target="_blank" title="Spesies atau jenis adalah suatu takson yang dipakai dalam taksonomi untuk menunjuk pada satu atau beberapa kelompok individu (populasi) yang serupa dan dapat saling membuahi satu sama lain di dalam kelompoknya (saling membagi gen) namun tidak dapat dengan anggota kelompok yang lain.">spesies</a> 
				organisme termasuk di dalamnya, tidak termasuk alga hijau. Dari jumlah itu, 258.650 jenis merupakan 
				<a href="https://id.wikipedia.org/wiki/Tumbuhan_berbunga" target="_blank" title="Tumbuhan berbunga atau Anthophyta (&quot;tumbuhan bunga&quot;) atau Magnoliophyta (&quot;tumbuhan sekerabat dengan magnolia&quot;) adalah kelompok terbesar tumbuhan yang hidup di daratan.">tumbuhan berbunga</a> 
				dan 18.000 jenis 
				<a href="https://id.wikipedia.org/wiki/Tumbuhan_lumut" target="_blank" title="Tumbuhan lumut merupakan sekumpulan tumbuhan kecil yang termasuk dalam Bryophytina (dari bahasa Yunani bryum, &quot;lumut&quot;).">tumbuhan lumut</a>. 
				Tumbuhan hijau menghasilkan hampir seluruh molekul oksigen di muka bumi ini dan merupakan bagian terpenting dalam sistem ekologi bumi. Tumbuhan-tumbuhan yang sudah di 
				<a href="https://id.wikipedia.org/wiki/Domestikasi" target="_blank" title="Kata &quot;Domestikasi&quot; berasal dari kata Latin Domus, yang berarti &quot;rumah&quot;.">domestikasi</a> 
				bisa menghasilkan biji, buah-buahan dan sayuran yang berguna sebagai bahan dasar pangan manusia. Selain itu tumbuhan juga digunakan sebagai tanaman hiasan dan banyak yang berkhasiat obat serta digunakan dalam ilmu 
				<a href="https://id.wikipedia.org/wiki/Kedokteran" target="_blank" title="Kedokteran (bahasa Inggris:medicine) adalah ilmu dan praktik dari diagnosis, pengobatan, dan pencegahan penyakit.">medis</a>. 
				Ilmu mengenai studi tanaman disebut 
				<a href="https://id.wikipedia.org/wiki/Botani" target="_blank" title="Botani adalah ilmu tumbuh-tumbuhan, termasuk juga jamur dan alga dengan mikologi dan fikologi berada di dalam cabang ilmu botani.">botani</a>, 
				yakni salah satu cabang ilmu 
				<a href="https://id.wikipedia.org/wiki/Biologi" target="_blank" title="Biologi adalah kajian tentang kehidupan, dan organisme hidup, termasuk struktur, fungsi, pertumbuhan, evolusi, persebaran, dan taksonominya.">biologi</a>.
			</p>
