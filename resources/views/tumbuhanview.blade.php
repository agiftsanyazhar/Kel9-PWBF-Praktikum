				<a href="https://id.wikipedia.org/wiki/Fungi" target="_blank" title="Fungi adalah nama regnum dari sekelompok besar makhluk hidup eukariotik heterotrof yang mencerna makanannya di luar tubuh lalu menyerap molekul nutrisi ke dalam sel-selnya.">fungi</a> 
