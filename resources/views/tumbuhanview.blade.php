			<h2 id="toc2">
				Ciri-Ciri Khas
			</h2>
			<p class="paragraf">
				Ciri yang segera mudah dikenali pada tumbuhan adalah warna kehijauan tapi bisa kuning yang dominan akibat kandungan pigmen klorofil yang berperan vital dalam proses penangkapan energi melalui fotosintesis. Dengan demikian, tumbuhan secara umum bersifat autotrof. Beberapa perkecualian, seperti pada sejumlah 
				<a href="https://id.wikipedia.org/wiki/Tumbuhan_parasit" target="_blank" title="Tumbuhan parasit adalah tumbuhan yang untuk kelangsungan hidupnya menggantungkan sebagian atau seluruh sumber energinya pada tumbuhan lain (disebut tumbuhan inang) dan mengakibatkan inangnya mengalami kekurangan energi.">tumbuhan parasit</a>, 
				merupakan akibat adaptasi terhadap cara hidup dan lingkungan yang unik. Karena sifatnya yang autotrof, tumbuhan selalu menempati posisi pertama dalam rantai aliran energi melalui organisme hidup (<a href="https://id.wikipedia.org/wiki/Rantai_makanan" target="_blank" title="Rantai makanan adalah perpindahan energi makanan dari sumber daya tumbuhan melalui seri organisme atau melalui jenjang makan.">rantai makanan</a>).
			</p>
			<p class="paragraf">
				Tumbuhan bersifat stasioner atau tidak bisa berpindah atas kehendak sendiri, meskipun beberapa alga hijau bersifat motil (mampu berpindah) karena memiliki 
				<a href="https://id.wikipedia.org/wiki/Flagelum" target="_blank" title="Flagelum (jamak flagela) adalah alat gerak (motile organ) berbentuk cambuk pada sejumlah organisme bersel satu.">flagelum</a>. 
				Akibat sifatnya yang pasif ini tumbuhan harus beradaptasi secara fisik atas perubahan lingkungan dan gangguan yang diterimanya. Variasi 
				<a href="https://id.wikipedia.org/wiki/Morfologi" target="_blank" title="Morfologi dipakai oleh berbagai cabang ilmu.">morfologi</a> 
				tumbuhan jauh lebih besar daripada anggota kerajaan lainnya. Selain itu, tumbuhan menghasilkan banyak sekali 
